// ***Association***
// 
// * A line means that there is an association between classes
// 
// * With addition of arrow heads, we have movement (info) i.e. who knows about who.
// 
// ------------------------------------------------
// UML:
// ------------------------------------------------
// 
// * Boxes:
//  * name
// 
//  * fields
// 
//  * operations
// 
// * Associations (arrows)
// 
//  * Show that two items are "connected", or "associated" with each other.
// 
//  * Multiplicities - constrain the number of allowable links in an association (1..m &lt;=&gt; 0&le;n&le;m)
//  
//  * Association class - link attributes (i.e. link members passed between one class and another)
//  
// * Aggregation (clear diamonds)
// 
//  * "Part of" relation between an aggregate (collection) and its members. ("Purchase Order" has an aggregation of "products")
//  
// * Composition (opaque diamonds)
// 
//  * "Part of" relation between a composite object and its components. ("catalog item" is part of a "catalog")
//  
// * Generalization (clear triangles):
//  
//  * Sub-type (parent/child class relationship)
//  
